/// 8-point grid spacing constants for the Panel Care design system.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;
  static const double xxxl = 64.0;

  // Semantic aliases
  static const double screenPadding = md;         // 16 horizontal page padding
  static const double cardPadding = md;           // 16 internal card padding
  static const double cardPaddingLg = lg;         // 24 large card padding
  static const double sectionSpacing = lg;        // 24 between sections
  static const double itemSpacing = sm;           // 8 between list items
  static const double iconTextGap = sm;           // 8 between icon and text
  static const double buttonHeight = 52.0;        // Standard button height
  static const double bottomNavHeight = 72.0;     // Nav bar height
  static const double headerHeight = 60.0;        // App header height
}
